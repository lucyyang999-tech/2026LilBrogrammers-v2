package frc.robot.subsystems;

import com.revrobotics.spark.SparkLowLevel.MotorType;
import com.revrobotics.spark.SparkMax;

import edu.wpi.first.wpilibj2.command.Command;
import edu.wpi.first.wpilibj2.command.SubsystemBase;
import frc.robot.Constants.OuttakeConstants;

public class Outtake extends SubsystemBase {
  /** Creates a new Outtake. */
  private SparkMax outtakeMotorTop;
  public Outtake() {
    outtakeMotorTop = new SparkMax(OuttakeConstants.outtakeMotorID, MotorType.kBrushless);


  }
  public Command spinOuttake() {
    return run(() -> {
      outtakeMotorTop.set(OuttakeConstants.outtakeSpeed);
    });
  }
  
  public Command stopOuttake() {
    return run(() -> {
      outtakeMotorTop.stopMotor();
    });
  }

  public Command reverseOuttake() {
    return run(() -> {
      outtakeMotorTop.set(-OuttakeConstants.outtakeSpeed);
    });
  }

  @Override
  public void periodic() {
    // This method will be called once per scheduler run
  }
}
